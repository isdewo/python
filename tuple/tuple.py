# 튜플은 N개의 요소로 이루어진 집합으로 데이터 변경이 불가함
a = ('apple', 'banana', 'orange')
print('a = ', a)
print('type(a) = ', type(a))

b = ('1', '2', '3', '4')
print('b = ', b)
print('type(b) = ', type(b))

# 요소가 1개인 튜플
c = (1,)
print('c = ', c)
print('type(c) = ', type(c))

# 이건 int
d = (1)
print('d = ', d)
print('type(d) = ', type(d))